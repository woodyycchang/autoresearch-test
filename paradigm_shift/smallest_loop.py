def load_config() -> tuple[dict, dict, dict]:
    """Return (rules, gate_params, inputs).

    gate_params are read from the inherited run_013 spec so the gates here are
    literally the run_013 gates.
    """
    rules = json.loads(RULES_PATH.read_text())
    inherit_path = THIS_DIR.parent / rules["inherit_gates_from"]
    spec = json.loads(inherit_path.read_text())
    gate_params = {
        "composite_threshold": spec["composite_threshold"],
        "quarantined_atoms": spec["quarantined_atoms"],
        "min_web_search_per_candidate": spec["min_web_search_per_candidate"],
        "belinda_strict": spec["belinda_strict"],
        "_inherited_from": str(inherit_path),
    }
    inputs = json.loads(INPUTS_PATH.read_text())
    return rules, gate_params, inputs



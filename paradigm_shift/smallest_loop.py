#!/usr/bin/env python3
"""SMALLEST proof-of-loop orchestrator for the Paradigm-Shift Finder.

Goal: prove the core loop works WITHOUT hallucination before scaling to the
7-epoch / 200-candidate run_013 configuration. Scope is deliberately tiny:
1 epoch, 3 candidates, 1 real Opus subprocess merge per candidate.

The loop proves four things, each with a hard, mechanical check:

  1. Opus subprocess merge -> REAL candidate (not mock concat).
     Each candidate is generated by `claude -p --model opus`. The full JSON
     envelope (result text + session_id + token usage + cost) is captured
     verbatim. A session_id and token usage cannot be fabricated in-context,
     so a [REPORT] block is proof the call really happened.

  2. [REPORT] injection shows raw ground truth.
     Every Opus envelope is written verbatim into logs/report_log.md under a
     [REPORT] tag. Downstream steps read the parsed candidate, but the raw
     envelope stays as the immutable ground truth.

  3. The 4-gate filter runs DETERMINISTICALLY.
     The merge is the stochastic generation step; the 4 gates are a pure
     function of the frozen candidate record. We run the gates twice over the
     frozen records and assert the two verdict hashes are byte-identical.

  4. No hallucination: the LLM summary matches the [REPORT] raw log.
     One final Opus call reads ONLY the raw log + gate results and re-states
     the key numbers. A pure-Python checker diffs those numbers field-by-field
     against the ground truth. Any divergence => HALLUCINATION_DETECTED, and
     the run exits non-zero.

The four gates are inherited verbatim from run_013's harness_rules.json, so
this is the same filter at the smallest possible scale.

Usage:
    python3 paradigm_shift/smallest_loop.py            # full run (real Opus x4)
    python3 paradigm_shift/smallest_loop.py --no-opus  # gates-only dry run
"""
